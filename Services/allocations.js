const express = require("express");
const dayjs = require("dayjs");
const router = express.Router();
const Joi = require("joi");
const { getAllocations, createAllocations, checkforDuplicate, insertFromWealth } = require("../DataModels/allocationModel");
const axios = require("axios");
const NodeCache = require("node-cache");
const myCache = new NodeCache();
const validatorMiddleWare = require("../Middleware/requestValidator");
const { symbol } = require("joi");

const schema = {
        get: Joi.object().keys({
                account_id: Joi.number().required(),
                mode: Joi.string().valid("all", "past", "current").required(),
        }),
        post: Joi.object().keys({
                account_id: Joi.number().required(),

                ticker: Joi.string().required(),
                exchange: Joi.string().required(),
                allocation: Joi.number().required().max(100),
                type: Joi.string().valid("Regular", "Single").required(),
                start: Joi.string().required(),
                end: Joi.string().optional(),
        }),
};
router.get("/", validatorMiddleWare(schema.get), async (req, res) => {
        const { account_id, mode } = req.validator;
        try {
                const allocation = await getAllocations(account_id, mode);
                if (allocation.length > 0) {
                        let tickerMap = {};
                        let totalPurchase = 0;
                        allocation.forEach((e) => {
                                tickerMap[`${e.ticker}.${e.exchange}`] = e;
                                totalPurchase += e.purchased_amount;
                        });
                        const yapiKeys = Object.keys(tickerMap).join(",");
                        var config = {
                                method: "GET",
                                url: `${process.env.FINANCE_ENDPOINT}${yapiKeys}`,
                                headers: {
                                        accept: "application/json",
                                        "x-api-key": process.env.FINANCE_KEY,
                                },
                        };
                        let financeApi = null;
                        const cacheValue = myCache.get(yapiKeys);
                        if (cacheValue) {
                                financeApi = { data: cacheValue };
                        } else {
                                financeApi = await axios(config);
                                myCache.set(yapiKeys, financeApi.data, 600);
                        }
                        if (financeApi.data && financeApi.data.quoteResponse && financeApi.data.quoteResponse.result) {
                                let formatedresult = financeApi.data.quoteResponse.result.map((e) => {
                                        let allocationVal = tickerMap[e.symbol];
                                        const reserve = allocationVal.purchase_required - allocationVal.purchased_amount;
                                        const current_value = allocationVal.purchased_quantity * e.regularMarketPreviousClose;
                                        return {
                                                quoteType: e.quoteType,
                                                currency: e.currency,
                                                timeZone: e.exchangeTimezoneName,
                                                status: e.marketState,
                                                name: e.shortName,
                                                ...allocationVal,
                                                price: e.regularMarketPreviousClose,
                                                changePercent: e.regularMarketChangePercent,
                                                reserve: reserve,
                                                canbuy: allocationVal.purchase_options === "Full" ? reserve > e.regularMarketPreviousClose : true,
                                                current_value: current_value,
                                                realization: current_value - allocationVal.purchased_amount,
                                                current_holdings: allocationVal.purchased_amount / totalPurchase,
                                        };
                                });
                                res.json(formatedresult);
                        } else {
                                res.status(422).send();
                        }
                } else {
                        res.json(allocation);
                }
        } catch (error) {
                console.log("====================================");
                console.log(error);
                console.log("====================================");
                res.status(500).send();
        }
});

router.post("/", validatorMiddleWare(schema.post), async (req, res) => {
        const { ticker, exchange, allocation, type, start, end, account_id } = req.validator;
        try {
                const allocationResult = await createAllocations(ticker, exchange, allocation, type, start, end, account_id);
                res.json(allocationResult);
        } catch (error) {
                console.log("====================================");
                console.log(error);
                console.log("====================================");
                res.status(500).send();
        }
});
